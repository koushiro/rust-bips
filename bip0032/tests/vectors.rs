fn assert_invalid_xpub<B: Secp256k1Backend>(keys: &[&str]) {
    for &key in keys {
        let parsed = key.parse::<ExtendedKeyPayload>();
        let is_invalid = match parsed {
            Ok(parsed) => ExtendedPublicKey::<B>::try_from(parsed).is_err(),
            Err(_) => true,
        };
        assert!(is_invalid);
    }
}

fn assert_invalid_xprv<B: Secp256k1Backend>(keys: &[&str]) {
    for &key in keys {
        let parsed = key.parse::<ExtendedKeyPayload>();
        let is_invalid = match parsed {
            Ok(parsed) => ExtendedPrivateKey::<B>::try_from(parsed).is_err(),
            Err(_) => true,
        };
        assert!(is_invalid);
    }
}

fn assert_invalid_case_for_all_backends(xpub_keys: &[&str], xprv_keys: &[&str]) {
    #[cfg(feature = "k256")]
    {
        assert_invalid_xpub::<K256Backend>(xpub_keys);
        assert_invalid_xprv::<K256Backend>(xprv_keys);
    }
    #[cfg(feature = "k256ecdsa")]
    {
        assert_invalid_xpub::<K256EcdsaBackend>(xpub_keys);
        assert_invalid_xprv::<K256EcdsaBackend>(xprv_keys);
    }
    #[cfg(feature = "secp256k1")]
    {
        assert_invalid_xpub::<Secp256k1FfiBackend>(xpub_keys);
        assert_invalid_xprv::<Secp256k1FfiBackend>(xprv_keys);
    }
    #[cfg(feature = "libsecp256k1")]
    {
        assert_invalid_xpub::<Libsecp256k1Backend>(xpub_keys);
        assert_invalid_xprv::<Libsecp256k1Backend>(xprv_keys);
    }
}

